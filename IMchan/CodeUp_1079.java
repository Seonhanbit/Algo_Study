package a20번전까지;

import java.util.Scanner;

public class CodeUp_1079 {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		char chr = 'q';
		while (true) {
			char cha = sc.next().charAt(0);
			System.out.println(cha);
			if (cha == chr) {
				break;
			}
			
		}
	}
}
